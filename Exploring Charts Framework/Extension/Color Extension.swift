    static func getRandomHTMLColors(n: Int) -> [Color] {
        var colors: [Color] = []

        for _ in 0 ..< n {
            var color = htmlColors.randomElement() ?? .black
            while colors.contains(color), n <= htmlColors.count {
                color = htmlColors.randomElement() ?? .black
            }
            colors.append(color)
        }

        return colors
    }
